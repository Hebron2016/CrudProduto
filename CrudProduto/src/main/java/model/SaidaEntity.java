package model;

public class SaidaEntity {
	private int saiCodTransacao;
	private int saiCodProduto;
	private int saiQtd;
	private int saiValorTotal;
	
	public int getSaiCodTransacao() {
		return saiCodTransacao;
	}
	public void setSaiCodTransacao(int saiCodTransacao) {
		this.saiCodTransacao = saiCodTransacao;
	}
	
	public int getSaiCodProduto() {
		return saiCodProduto;
	}
	public void setSaiCodProduto(int saiCodProduto) {
		this.saiCodProduto = saiCodProduto;
	}
	
	public int getSaiQtd() {
		return saiQtd;
	}
	public void setSaiQtd(int saiQtd) {
		this.saiQtd = saiQtd;
	}
	
	public int getSaiValorTotal() {
		return saiValorTotal;
	}
	public void setSaiValorTotal(int saiValorTotal) {
		this.saiValorTotal = saiValorTotal;
	}
}
